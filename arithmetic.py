def add(num1, num2):
    return num1 + num2 

def subtract(num1, num2):
    num1 = float(int(num1))
    num2 = float(int(num2)) 
    return num1 - num2 

def multiply(num1, num2):
    return num1 * num2

def divide(num1, num2):
    return float(int(num1 / num2)) 

def square(num1):
    return num1 ** 2

def cube(num1):
    return num1 ** 3

def power(num1, num2):
    return num1 ** num2

def mod(num1, num2):
    return num1 % num2
